#include <bits/stdc++.h>
using namespace std;
#define ull unsigned long long
#define ll long long

int main() {
	ll n = 0;
	cin >> n;
	if (n % 2 == 0) cout << n / 2 << endl;
	else cout << -((n / 2) + 1) << endl;
	return 0;
}